%Метод Симпсона
disp('Интеграл функции a методом Симпсона: ');
q1 = quad('(2 .*sqrt(3)) ./(sqrt(3 - 9 .* x .^ 2) .* asin(sqrt(3) .* x))',a1,b1,h);
disp(q1);
disp('Интеграл функции b методом Симпсона: ');
q2 = quad('sin(x) ./ x .^ 3',a1,b1,h);
